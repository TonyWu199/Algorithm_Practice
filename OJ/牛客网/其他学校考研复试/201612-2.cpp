#include <iostream>

using namespace std;

int main(){
	int as, as1;
	cin >> as1;
	
	for(int i=0; i<=100000; i += 100){
		if(i<=3500){
			as = i;	
		}else if(i>3500 && i<=5000){  //0~1500
			as = 3500 + 0.97 * (i-3500);
		}else if(i>5000 && i<=8000){  //1500~4500
			as = 3500 + 0.97 * 1500 + 0.9 * (i-5000);
		}else if(i>8000 && i<=12500){
			as = 3500 + 0.97 * 1500 + 0.9 * 3000 + 0.8 * (i-8000); 
		}else if(i>12500 && i<=38500){
			as = 3500 + 0.97 * 1500 + 0.9 * 3000 + 0.8 * 4500 + 0.75 * (i-12500);
		}else if(i>38500 && i<=58500){
			as = 3500 + 0.97 * 1500 + 0.9 * 3000 + 0.8 * 4500 + 0.75 * 26000 + 0.7 * (i-38500);
		}else if(i>58500 && i<=83500){
			as = 3500 + 0.97 * 1500 + 0.9 * 3000 + 0.8 * 4500 + 0.75 * 26000 + 0.7 * 20000 + 0.65 * (i-58500);
		} else if(i>83500){
			as = 3500 + 0.97 * 1500 + 0.9 * 3000 + 0.8 * 4500 + 0.75 * 26000 + 0.7 * 20000 + 0.65 * 25000 + 0.55 * (i-83500);
		}
		
		if(as == as1){
			cout << i << endl;
			break;
		}
	}
} 
